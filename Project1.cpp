#include <iostream>

using namespace std;

int main()
{
	cout << "Nhap So Nguyen A: ";
	short a;
	cin >> a;
	cout << "Nhap So Nguyen B: ";
	short b;
	cin >> b;

	cout << a << " + " << b << " = " << a + b << endl;
	cout << a << " - " << b << " = " << a - b << endl;
	cout << a << " * " << b << " = " << a * b << endl;
	cout << a << " / " << b << " = " << a / b << endl;
	return 0;


}